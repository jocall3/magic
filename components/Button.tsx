/**
 * Button Component Props Interface
 * Defines the expected properties for the Button component.
 */
interface ButtonProps extends React.ButtonHTMLAttributes<HTMLButtonElement> {
  /**
   * The visual style of the button. Can be one of the predefined variants.
   * @default "default"
   */
  variant?: keyof typeof buttonVariants.variant;
  /**
   * The size of the button. Can be one of the predefined sizes.
   * @default "default"
   */
  size?: keyof typeof buttonVariants.size;
  /**
   * Whether the button should display a loading spinner.
   * @default false
   */
  isLoading?: boolean;
  /**
   * The content to display inside the button when it's loading.
   * Defaults to a spinner component.
   */
  loadingContent?: React.ReactNode;
  /**
   * Whether the button should be disabled.
   * @default false
   */
  disabled?: boolean;
  /**
   * The content to display inside the button.
   */
  children: React.ReactNode;
}

/**
 * Button Component
 *
 * A highly customizable and visually distinct button component designed for
 * modern web applications. It supports various themes, sizes, and states,
 * including loading and disabled states. The component is built with a focus
 * on accessibility and a robust design system.
 *
 * @param {ButtonProps} props - The properties for the Button component.
 * @returns {JSX.Element} The rendered Button component.
 */
const Button: React.FC<ButtonProps> = ({
  variant = "default",
  size = "default",
  isLoading = false,
  loadingContent = <Spinner />,
  children,
  className,
  disabled,
  ...props
}) => {
  const baseStyles =
    "inline-flex items-center justify-center font-semibold rounded-md focus:outline-none focus:ring-2 focus:ring-offset-2 transition-all duration-150 ease-in-out disabled:opacity-50 disabled:cursor-not-allowed";
  
  const variantStyles = buttonVariants.variant[variant] || "";
  const sizeStyles = buttonVariants.size[size] || "";

  const combinedStyles = cn(
    baseStyles,
    variantStyles,
    sizeStyles,
    className // Allow for custom class names to override or extend styles
  );

  return (
    <button
      className={combinedStyles}
      disabled={disabled || isLoading}
      {...props}
    >
      {isLoading ? (
        <span className="flex items-center justify-center">
          {loadingContent}
          <span className="ml-2">Loading...</span>
        </span>
      ) : (
        children
      )}
    </button>
  );
};

export default Button;