const QuantumChatUI: FC = () => {
  const context = useContext(StripeDataContext);
  const [input, setInput] = useState('');
  const [isOpen, setIsOpen] = useState(false);

  if (!context) return null;

  const handleSend = (e: React.FormEvent) => {
    e.preventDefault();
    if (!input.trim() || context.isAiProcessing) return;
    context.sendMessage(input);
    setInput('');
  };

  return (
    <div className={`fixed bottom-6 right-6 z-50 transition-all duration-500 ${isOpen ? 'w-96 h-[500px]' : 'w-16 h-16'}`}>
      {!isOpen ? (
        <button 
          onClick={() => setIsOpen(true)}
          className="w-full h-full bg-gradient-to-br from-cyan-500 to-blue-600 rounded-full shadow-[0_0_20px_rgba(6,182,212,0.5)] flex items-center justify-center hover:scale-110 transition-transform border border-cyan-400/50"
        >
          <svg xmlns="http://www.w3.org/2000/svg" className="h-8 w-8 text-white" fill="none" viewBox="0 0 24 24" stroke="currentColor">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M8 10h.01M12 10h.01M16 10h.01M9 16H5a2 2 0 01-2-2V6a2 2 0 012-2h14a2 2 0 012 2v8a2 2 0 01-2 2h-5l-5 5v-5z" />
          </svg>
        </button>
      ) : (
        <div className="w-full h-full bg-slate-900 border border-cyan-500/30 rounded-2xl shadow-2xl flex flex-col overflow-hidden backdrop-blur-xl">
          <div className="p-4 bg-gradient-to-r from-slate-800 to-slate-900 border-b border-cyan-500/20 flex justify-between items-center">
            <div className="flex items-center gap-2">
              <div className="w-2 h-2 bg-cyan-400 rounded-full animate-pulse" />
              <span className="text-xs font-bold tracking-widest text-cyan-400 uppercase">Quantum AI Core</span>
            </div>
            <button onClick={() => setIsOpen(false)} className="text-slate-400 hover:text-white">
              <svg xmlns="http://www.w3.org/2000/svg" className="h-5 w-5" viewBox="0 0 20 20" fill="currentColor">
                <path fillRule="evenodd" d="M4.293 4.293a1 1 0 011.414 0L10 8.586l4.293-4.293a1 1 0 111.414 1.414L11.414 10l4.293 4.293a1 1 0 01-1.414 1.414L10 11.414l-4.293 4.293a1 1 0 01-1.414-1.414L8.586 10 4.293 5.707a1 1 0 010-1.414z" clipRule="evenodd" />
              </svg>
            </button>
          </div>
          
          <div className="flex-1 overflow-y-auto p-4 space-y-4 scrollbar-thin scrollbar-thumb-cyan-900">
            {context.chatMessages.map(msg => (
              <div key={msg.id} className={`flex ${msg.role === 'user' ? 'justify-end' : 'justify-start'}`}>
                <div className={`max-w-[80%] p-3 rounded-xl text-sm ${
                  msg.role === 'user' 
                    ? 'bg-cyan-600 text-white rounded-br-none' 
                    : 'bg-slate-800 text-slate-200 border border-slate-700 rounded-bl-none'
                }`}>
                  {msg.content}
                </div>
              </div>
            ))}
            {context.isAiProcessing && (
              <div className="flex justify-start">
                <div className="bg-slate-800 p-3 rounded-xl flex gap-1">
                  <div className="w-1.5 h-1.5 bg-cyan-500 rounded-full animate-bounce" />
                  <div className="w-1.5 h-1.5 bg-cyan-500 rounded-full animate-bounce [animation-delay:0.2s]" />
                  <div className="w-1.5 h-1.5 bg-cyan-500 rounded-full animate-bounce [animation-delay:0.4s]" />
                </div>
              </div>
            )}
          </div>

          <form onSubmit={handleSend} className="p-4 bg-slate-800/50 border-t border-slate-700">
            <div className="relative">
              <input 
                type="text"
                value={input}
                onChange={(e) => setInput(e.target.value)}
                placeholder="Ask the Quantum Core..."
                className="w-full bg-slate-950 border border-slate-700 rounded-lg py-2 px-4 text-sm focus:outline-none focus:border-cyan-500 transition-colors pr-10"
              />
              <button type="submit" className="absolute right-2 top-1/2 -translate-y-1/2 text-cyan-500 hover:text-cyan-400">
                <svg xmlns="http://www.w3.org/2000/svg" className="h-5 w-5" viewBox="0 0 20 20" fill="currentColor">
                  <path d="M10.894 2.553a1 1 0 00-1.788 0l-7 14a1 1 0 001.169 1.409l5-1.429A1 1 0 009 15.571V11a1 1 0 112 0v4.571a1 1 0 00.725.962l5 1.428a1 1 0 001.17-1.408l-7-14z" />
                </svg>
              </button>
            </div>
          </form>
        </div>
      )}
    </div>
  );
};

const AuditLogPanel: FC = () => {
  const context = useContext(StripeDataContext);
  const [isExpanded, setIsExpanded] = useState(false);

  if (!context) return null;

  return (
    <div className={`fixed left-6 bottom-6 z-40 transition-all duration-500 ${isExpanded ? 'w-[500px] h-[400px]' : 'w-48 h-10'}`}>
      <div className="w-full h-full bg-black/80 border border-red-500/20 rounded-lg backdrop-blur-md flex flex-col overflow-hidden">
        <button 
          onClick={() => setIsExpanded(!isExpanded)}
          className="p-2 bg-red-950/20 flex items-center justify-between text-[10px] font-mono tracking-tighter text-red-400 uppercase"
        >
          <span className="flex items-center gap-2">
            <span className="w-1.5 h-1.5 bg-red-500 rounded-full animate-pulse" />
            Secure Audit Storage
          </span>
          <span>{isExpanded ? 'Collapse' : 'Expand'}</span>
        </button>
        
        {isExpanded && (
          <div className="flex-1 overflow-y-auto p-3 font-mono text-[10px] space-y-2">
            <div className="flex justify-between border-b border-red-500/10 pb-1 mb-2">
              <span className="text-red-500/50">SYSTEM_UPTIME: 99.999%</span>
              <button onClick={context.clearLogs} className="hover:text-white text-red-500 underline">WIPE_TRAIL</button>
            </div>
            {context.auditLogs.length === 0 && <div className="text-slate-600 italic">No sensitive actions recorded in current session.</div>}
            {context.auditLogs.map(log => (
              <div key={log.id} className="border-l border-red-500/30 pl-2 py-1">
                <div className="flex justify-between text-red-500/70">
                  <span>[{log.timestamp.split('T')[1].split('.')[0]}] {log.action}</span>
                  <span className={log.severity === 'CRITICAL' ? 'text-red-500 font-bold' : ''}>{log.severity}</span>
                </div>
                <div className="text-slate-400 truncate">{log.details}</div>
              </div>
            ))}
          </div>
        )}
      </div>
    </div>
  );
};

