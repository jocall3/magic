type QuantumResourcesType = typeof quantumResourcesData;

interface StripeDataContextType {
  stripeData: QuantumResourcesType; // Kept as stripeData for compatibility
  setStripeData: React.Dispatch<React.SetStateAction<QuantumResourcesType>>;
  auditLogs: AuditLog[];
  chatMessages: ChatMessage[];
  isAiProcessing: boolean;
  sendMessage: (content: string) => Promise<void>;
  logAction: (action: string, details: string, severity?: AuditLog['severity']) => void;
  clearLogs: () => void;
}

const StripeDataContext = createContext<StripeDataContextType | undefined>(undefined);

export const StripeDataProvider: FC<{ children: ReactNode }> = ({ children }) => {
  const [stripeData, setStripeData] = useState<QuantumResourcesType>(quantumResourcesData);
  const [auditLogs, setAuditLogs] = useState<AuditLog[]>([]);
  const [chatMessages, setChatMessages] = useState<ChatMessage[]>([
    {
      id: 'welcome',
      role: 'assistant',
      content: "Welcome to the Quantum Financial Demo. I am your AI Treasury Assistant. You have the 'Golden Ticket' to explore our high-performance engine. How can I help you manage your capital today?",
      timestamp: new Date().toISOString()
    }
  ]);
  const [isAiProcessing, setIsAiProcessing] = useState(false);

  // --- AUDIT LOGGING SYSTEM ---
  const logAction = useCallback((action: string, details: string, severity: AuditLog['severity'] = 'INFO') => {
    const newLog: AuditLog = {
      id: `log_${Date.now()}_${Math.random().toString(36).substr(2, 5)}`,
      timestamp: new Date().toISOString(),
      action,
      actor: 'SYSTEM',
      details,
      severity,
      ipAddress: '127.0.0.1 (Secure Proxy)'
    };
    setAuditLogs(prev => [newLog, ...prev].slice(0, 100));
    console.log(`[AUDIT] ${action}: ${details}`);
  }, []);

  const clearLogs = () => {
    setAuditLogs([]);
    logAction('LOGS_CLEARED', 'User cleared the audit trail.');
  };

  // --- AI INTEGRATION (GEMINI) ---
  const sendMessage = async (content: string) => {
    const userMsg: ChatMessage = {
      id: `msg_${Date.now()}`,
      role: 'user',
      content,
      timestamp: new Date().toISOString()
    };

    setChatMessages(prev => [...prev, userMsg]);
    setIsAiProcessing(true);
    logAction('AI_QUERY', `User asked: "${content.substring(0, 50)}..."`);

    try {
      const apiKey = process.env.GEMINI_API_KEY || "";
      if (!apiKey) {
        throw new Error("GEMINI_API_KEY is not configured in the secrets manager.");
      }

      const genAI = new GoogleGenAI(apiKey);
      const model = genAI.getGenerativeModel({ model: "gemini-1.5-flash" });

      const systemPrompt = `
        You are the Quantum Financial AI Core. 
        The user is in a "Golden Ticket" demo environment. 
        Tone: Elite, Professional, High-Performance.
        Context: This is a business banking demo for a global institution.
        Rules:
        1. Never use the name "Citibank". Use "Quantum Financial" or "The Demo Bank".
        2. You can "simulate" actions. If the user asks to create a payment, say you've initiated it and describe the high-performance rails (Wire/ACH).
        3. Mention "Bells and Whistles" and "Test Driving the Engine" occasionally.
        4. You have access to mock data: Balance is $425M, Threat level is Low, Cash flow is positive.
        5. If the user asks to "create" something, respond with a confirmation that the Quantum Core has processed the request.
      `;

      const result = await model.generateContent([systemPrompt, ...chatMessages.map(m => `${m.role}: ${m.content}`), `user: ${content}`]);
      const responseText = result.response.text();

      const aiMsg: ChatMessage = {
        id: `msg_ai_${Date.now()}`,
        role: 'assistant',
        content: responseText,
        timestamp: new Date().toISOString()
      };

      setChatMessages(prev => [...prev, aiMsg]);

      // Simulate "Creating the shit it needs"
      if (content.toLowerCase().includes('payment') || content.toLowerCase().includes('transfer')) {
        const newTx = {
          id: `q_txn_ai_${Date.now()}`,
          object: 'balance_transaction',
          amount: 50000,
          currency: 'usd',
          description: "AI-Initiated Strategic Disbursement",
          status: 'pending',
          type: 'wire',
          created: Date.now(),
          fee: 0,
          net: 50000,
          reporting_category: 'transfer'
        };
        setStripeData(prev => ({
          ...prev,
          transactions: [newTx, ...prev.transactions]
        }));
        logAction('AI_ACTION_EXECUTE', 'AI Core initiated a $50,000 Wire Transfer via Quantum Rails.', 'WARNING');
      }

    } catch (error: any) {
      console.error("AI Error:", error);
      setChatMessages(prev => [...prev, {
        id: `msg_err_${Date.now()}`,
        role: 'assistant',
        content: "I apologize, the Quantum Neural Link is experiencing high-frequency interference. Please try again.",
        timestamp: new Date().toISOString()
      }]);
    } finally {
      setIsAiProcessing(false);
    }
  };

  // --- PERSISTENCE SIMULATION ---
  useEffect(() => {
    logAction('SESSION_START', 'User entered the Golden Ticket Demo Environment.');
  }, [logAction]);

  const value = { 
    stripeData, 
    setStripeData, 
    auditLogs, 
    chatMessages, 
    isAiProcessing, 
    sendMessage, 
    logAction,
    clearLogs
  };

  return (
    <StripeDataContext.Provider value={value}>
      <div className="quantum-provider-wrapper relative min-h-screen bg-slate-950 text-slate-100 font-sans selection:bg-cyan-500/30">
        {children}
        
        {/* THE CHAT BAR - BELLS AND WHISTLES UI */}
        <QuantumChatUI />
        
        {/* AUDIT LOG OVERLAY - SECURITY NON-NEGOTIABLE */}
        <AuditLogPanel />
      </div>
    </StripeDataContext.Provider>
  );
};

